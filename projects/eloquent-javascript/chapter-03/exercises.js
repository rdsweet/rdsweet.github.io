//min is designed to determine the smaller number between 2 numbers
// if a is less than b return a
// if b is less than a return b
// otherwise return false
function min(a, b) { 
  if(a < b) {
    return a;
  } else if (b < a){
    return b;
  } else {
    return 0;
  }

}

