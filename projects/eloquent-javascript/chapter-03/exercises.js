//isEven designed to determine if a number is even
// if number + or - is divided by 2 gives you a remainder 0
//return true
// return false
//console even
function isEven(num) {
  if(num % 2 === 0 || -(num) %2 === 0){
    return true;
  } else {
    return false;
  }
  

}

