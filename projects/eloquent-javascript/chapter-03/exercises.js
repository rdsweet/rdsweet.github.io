// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//countChars - counts the specific number of character occurrences in a given string
//like in loose --the count for o's would be 2
//given a string
//given a character
//count the number of times a character occurs
function countChars(string, x) {
  var count = 0;
  for(var i = 0; i < string.length; i++){
        if(string[i] === x){
      count = count + 1;
    }
    
  }
  return count;
}

